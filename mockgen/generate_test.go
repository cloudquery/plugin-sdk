package mockgen

import (
	"context"
	"io"
	"os"
	"path"
	"reflect"
	"testing"

	"github.com/google/go-cmp/cmp"
)

type Client struct{}

func (*Client) ListThings() error {
	return nil
}

func (*Client) ListTables(_ context.Context) error {
	return nil
}

func (*Client) CreateTables(_ context.Context, _ []string) error {
	return nil
}

var wantOutput = `// Code generated by codegen; DO NOT EDIT.
package services

import (
	"github.com/cloudquery/plugin-sdk/mockgen"
	"net/http"
)

//go:generate mockgen -package=mocks -destination=../mocks/mockgen.go -source=mockgen.go MockgenClient
type MockgenClient interface {
	ListTables(context.Context) error
}
`

func TestGenerateInterfaces(t *testing.T) {
	dir := t.TempDir()
	err := GenerateInterfaces([]any{&Client{}}, dir, WithIncludeFunc(func(m reflect.Method) bool {
		return MethodHasAnyPrefix(m, []string{"List"}) && MethodHasAnySuffix(m, []string{"Tables"})
	}))
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	fileName := path.Join(dir, "mockgen.go")
	f, err := os.Open(fileName)
	if err != nil {
		t.Fatalf("failed to open file: %v", err)
	}
	b, err := io.ReadAll(f)
	if err != nil {
		t.Fatalf("failed to read file: %v", err)
	}
	if diff := cmp.Diff(string(b), wantOutput); diff != "" {
		t.Errorf("unexpected diff (-want +got):\n%s", diff)
	}
}
